pub fn part_one(input: &str) -> u32 {
    let total = input.lines().into_iter().map(|r| {
        let mut moves = r.split(" ");

        let elf_move = moves.nth(0).unwrap();
        let player_move = moves.nth(0).unwrap();

        let elf_move = get_hand_shape(elf_move);
        let player_move = get_hand_shape(player_move);
        let round_result = get_round_result(elf_move, &player_move);
        let round_score = get_round_score(player_move, round_result);

        round_score        
    }).sum();

    dbg!(total);

    total
}

pub fn part_two(input: &str) -> u32 {
    let total = input.split('\n').into_iter().map(|r| {
        let mut moves = r.split(" ");

        let elf_move = moves.nth(0).unwrap();
        let round_result = moves.nth(0).unwrap();

        let round_result = parse_round_result(round_result);
        let elf_move = get_hand_shape(elf_move);

        let player_move = get_hand_shape_for_result(elf_move, &round_result);
        let round_score = get_round_score(player_move, round_result);

        round_score
    }).sum();

    dbg!(total);

    total
}

fn get_round_score(hand_shape: HandShape, round_result: RoundResult) -> u32 {
    get_hand_shape_score(hand_shape) + get_round_result_score(round_result)
}

fn get_hand_shape_score(hand_shape: HandShape) -> u32 {
    match hand_shape {
        HandShape::Paper => 2u32,
        HandShape::Rock => 1u32,
        HandShape::Scissors => 3u32,
    }
}

fn get_hand_shape_for_result(elf_shape: HandShape, round_result: &RoundResult) -> HandShape {
    match (elf_shape, round_result) {
        (HandShape::Paper, RoundResult::Draw) => HandShape::Paper,
        (HandShape::Paper, RoundResult::Lose) => HandShape::Rock,
        (HandShape::Paper, RoundResult::Win) => HandShape::Scissors,
        (HandShape::Rock, RoundResult::Draw) => HandShape::Rock,
        (HandShape::Rock, RoundResult::Lose) => HandShape::Scissors,
        (HandShape::Rock, RoundResult::Win) => HandShape::Paper,
        (HandShape::Scissors, RoundResult::Draw) => HandShape::Scissors,
        (HandShape::Scissors, RoundResult::Lose) => HandShape::Paper,
        (HandShape::Scissors, RoundResult::Win) => HandShape::Rock,
    }
}

fn parse_round_result(code: &str) -> RoundResult {
    match code {
        "X" => RoundResult::Lose,
        "Y" => RoundResult::Draw,
        "Z" => RoundResult::Win,
        _ => panic!("Unexpected character"),
    }
}

fn get_round_result(elf_shape: HandShape, player_shape: &HandShape) -> RoundResult {
    match (elf_shape, player_shape) {
        (HandShape::Paper, HandShape::Paper) => RoundResult::Draw,
        (HandShape::Paper, HandShape::Rock) => RoundResult::Lose,
        (HandShape::Paper, HandShape::Scissors) => RoundResult::Win,
        (HandShape::Rock, HandShape::Paper) => RoundResult::Win,
        (HandShape::Rock, HandShape::Rock) => RoundResult::Draw,
        (HandShape::Rock, HandShape::Scissors) => RoundResult::Lose,
        (HandShape::Scissors, HandShape::Paper) => RoundResult::Lose,
        (HandShape::Scissors, HandShape::Rock) => RoundResult::Win,
        (HandShape::Scissors, HandShape::Scissors) => RoundResult::Draw,
    }
}

fn get_round_result_score(round_result: RoundResult) -> u32 {
    match round_result {
        RoundResult::Draw => 3u32,
        RoundResult::Lose => 0u32,
        RoundResult::Win => 6u32,
    }
}

fn get_hand_shape(code: &str) -> HandShape {
    match code {
        "A" | "X" => HandShape::Rock,
        "B" | "Y" => HandShape::Paper,
        "C" | "Z" => HandShape::Scissors,
        _ => panic!("Unexpected character"),
    }
}

enum HandShape {
    Paper,
    Rock,
    Scissors,
}

enum RoundResult {
    Draw,
    Lose,
    Win,
}

